#include "ft_printf.h"

void	ft_unsigned(unsigned int nb, int *len)
{
	if (nb < 10)
	{
		ft_putchar(nb + '0', len);
	}
	else
	{
		ft_unsigned(nb / 10, len);
		ft_unsigned(nb % 10, len);
	}
}
